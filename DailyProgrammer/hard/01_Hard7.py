# **Hard Exercises:**

# 1. Write a function to calculate the factorial of a given number using recursion.
# 2. Implement a program to solve the Tower of Hanoi problem using recursion.
# 3. Create a function to find the maximum sum of a subarray in a given list of integers (Kadane's algorithm).
# 4. Write a program to find the shortest path in a weighted graph using Dijkstra's algorithm.
# 5. Implement a function to perform matrix multiplication.
# 6. Create a program to sort a list of elements using the quicksort algorithm.
# 7. Write a function to solve the N-Queens problem, which places N queens on an NxN chessboard without attacking each other.
